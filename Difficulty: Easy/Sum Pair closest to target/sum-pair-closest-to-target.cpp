class Solution {
  public:
    vector<int> sumClosest(vector<int>& arr, int target) {
        vector<int> res;
        int closest_diff=1e9;
        sort(arr.begin(),arr.end());
        int start=0, end=arr.size()-1;
        while(start<end){
            int sum=arr[start]+arr[end];
            int d=abs(target-sum);
            if(d<closest_diff){
                closest_diff=d;
                res={arr[start],arr[end]};
            }
            if(sum<=target) start++;
            else end--;
        }
        return res;
    }


};

